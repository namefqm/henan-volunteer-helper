// 根据专业名称（含括号）反查主分类名称
function getMajorGroup(majorName) {
  if (!majorName) return null;
  // 去掉可能的开头数字编号，如 "01美术学" -> "美术学"
  const trimmed = majorName.replace(/^\d+/, '').replace(/^\(\)/, '').trim();
  // 1. 先精确匹配 MAJOR_GROUPS
  for (const main in MAJOR_GROUPS) {
    if (MAJOR_GROUPS[main].includes(trimmed) || MAJOR_GROUPS[main].includes(majorName)) return main;
  }
  // 2. 去掉括号内容，按基础名分组（如"书画艺术(书法)" → "书画艺术"）
  const baseName = trimmed.replace(/\(.*?\)/g, '').trim();
  // 有括号变体且基础名不为空 → 用基础名归组
  if (baseName && baseName !== trimmed) {
    return baseName;
  }
  // 3. 返回基础名（使未配置的专业也能合并）
  return baseName || trimmed;
}
